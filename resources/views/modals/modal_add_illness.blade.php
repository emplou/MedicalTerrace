            <h3>手足口病の症状</h3>
            </p>手のひらや足の裏に赤い発疹や水ぶくれができます。口の中にも赤い発疹や水ぶくれ（口内炎）ができます。次第に、手足全体、ひじ、ひざ、おしりに出ることもあります。高熱が出ることはあまりなく、発病しても、軽い症状だけで治ることが多くあります。また、夏風邪と言われますが、冬の風とは異なり、鼻水や咳の症状はあまりありません。<p>

            <h3>手足口病の原因</h3>
            </p>エンテロウイルス属のウイルスによる感染です。具体的にはコクサッキーウイルスA6、A16、エンテロウイルス71（EV71）などが挙げられます。感染経路には、飛沫感染、接触感染、糞口感染（便の中に排泄されたウイルスが口に入って感染する）があります。特に、保育施設や幼稚園など、乳幼児が集団生活をしている施設では注意が必要です。ウイルスの潜伏期があるため、施設で手足口病にかかった子どもがいると、集団感染が起こりやすくなります。原因となるウイルスに初めて感染する乳幼児がいると、感染経験がないため発病しやすくなります。<p>

            <h3>手足口病の合併症</h3>
            </p>まれですが、髄膜炎、小脳失調症、脳炎などの中枢神経系の症状のほか、心筋炎、神経原性肺水腫、急性弛緩性麻痺など、さまざまな合併症が起こる場合があります。<p>

            <h3>手足口病の自宅療法（療養方法、再発防止など）</h3>
            </p>口内炎があってうまく食事ができない場合は柔らかく、味の薄い、冷たい食事にします。食欲が落ちている時はこまめに水分をとるようにしましょう。<p>

            <h3>手足口病の予防・対策方法</h3>
            </p>有効なワクチンはありません。発病を予防する薬もありません。接触感染を予防するために、しっかり手洗いをすること、うがいをすることなどが基本の対策になります。手洗いは石けんを使い、流水で洗いましょう。治った後も2〜4週間ほど、ウイルスが便中に排泄されることがあるため、感染を広げないために、排泄物を適切に処理することが必要です。おむつ交換をする時は特に気をつけ、しっかり手を洗います。タオルの共用もしません。家庭では兄弟への感染に気をつけること、乳幼児の保育施設などの集団生活では、職員と子どもがしっかり対策をすることが大切です。<p>
            
